class Topology:
    def __init__(self, topology_dict):
        self.topology = self._normalize(topology_dict)

    def _normalize(self, topology_dict):
        # Функция min() и max() сравнивает кортежи local и remote.
        # Сравнение кортежей в Python происходит лексикографически (сначала по первому элементу,
        # затем, если они равны, по второму и т.д.).
        # Например, ("r1", "eth0/0") "меньше" чем ("sw1", "eth0/1"), потому что 'r1' меньше чем 'sw1' (по алфавиту).
        # Код использует эту логику, чтобы гарантировать, что для каждого физического соединения
        # (например, между r1 и sw1) в итоговом словаре topology будет создана только одна запись.
        # Меньший (лексикографический) из двух кортежей всегда становится ключом, а больший — значением.
        # Таким образом, дублирующиеся записи из topology_example (где стороны поменяны местами) игнорируются,
        # так как попытка добавить запись с уже существующим ключом просто перезапишет ее тем же значением.
        return {min(local, remote): max(local, remote) for local, remote in topology_dict.items()}
