# -*- coding: utf-8 -*-
'''
БЫЛО (все тесты проходит)
Описание:
Вы работаете над программой для автоматического оценивания работ студентов.
Требуется определить число студентов, получивших проходной балл по результатам проверки.
Проходной балл – 60. Максимальная оценка – 100 баллов.
Формат ввода:
Строка, содержащая разделенные пробелами целые числа в диапазоне от 0 до 100.
Формат вывода:
Строка формат «Прошли число_прошедших, не прошли число_не_прошедших».

Пример 1:
Входные данные:
40 50 60 70 80 90

Выходные данные:
Прошли 4, не прошли 2

Пример 2:
Входные данные:
78 91 0 12 66 32

Выходные данные:
Прошли 3, не прошли 3

Пример 3 (тест):
Входные данные:
30 40 50 60 70

Выходные данные:
Прошли 2, не прошли 3

Пример 4 (тест):
Входные данные:
55 65 75 85 95

Выходные данные:
Прошли 4, не прошли 1

Пример 5 (тест):
Входные данные:
60 60 60 60 60

Выходные данные:
Прошли 5, не прошли 0

Пример 6 (тест):
Входные данные:
45 55 65 75 85

Выходные данные:
Прошли 3, не прошли 2

Пример 7 (тест):
Входные данные:
10 20 30 40 50

Выходные данные:
Прошли 0, не прошли 5
'''

def passing_scores(scores: str) -> str:
    passed = []
    no_pass = []
    for item in scores.split():
        if int(item) >= 60:
            passed.append(item)
        else:
            no_pass.append(item)
    return f'Прошли {len(passed)}, не прошли {len(no_pass)}'

scores = input()
result = passing_scores(scores)
print(result)









